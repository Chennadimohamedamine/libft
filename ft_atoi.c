#include "libft.h"

int	ft_atoi(const char *str)
{
	long	r;
	int		s;

	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	s = 1;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			s *= -1;
		str++;
	}
	r = 0;
	while (*str >= '0' && *str <= '9')
	{
		r = r * 10 + (*str - 48);
		str++;
	}
	return (r * s);
}
